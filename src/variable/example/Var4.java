package variable.example;

public class Var4 {
    public static void main(String[] args) {
        int a;
        a = 10;

        int b;
        b = 20;

        int c = 30, d = 40, e = 50;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        System.out.println(a + " " + b + " " + c + " " + d + " " + e);
    }
}
