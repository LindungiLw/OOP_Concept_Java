package variable.practice;

public class VarPractice3 {
    public static void main(String[] args) {
        long l = 10000000000L;
        System.out.println(l);

        boolean b = true;
        System.out.println(b);

        char c = 'A';
        System.out.println(c);

        float f = 3.14f;
        System.out.println(f);

        double d = 3.14159;
        System.out.println(d);
    }
}
