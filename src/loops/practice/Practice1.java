package loops.practice;

public class Practice1 {
    public static void main(String[] args) {
        int count = 0;
        for (int i = 1; i <= 10; i++) {
            System.out.println(count + i);
        }

        int count2 = 0;
        while (count2 < 10){
            count2++;
            System.out.println(count2);
        }
    }
}
