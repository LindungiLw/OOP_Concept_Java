package loops.while_;

public class While1 {
    public static void main(String[] args) {
        int count = 0;
        count = count + 1;

        System.out.println("Current Number: " + count);
        count = count + 1;
        System.out.println("Current Number: " + count);
        count = count + 1;
        System.out.println("Current Number: " + count);

        count++;
        System.out.println("Current Number: " + count);

        ++count;
        System.out.println("Current Number: " + count);

        count += 1;
        System.out.println("Current Number: " + count);

        count += 2;
        System.out.println("Current Number: " + count);
    }
}
