public class Practice4 {
    public static void main(String[] args) {
        double rating = 7;

        if (rating <= 9){
            System.out.println("I recommend 'About Time'.");
        }
        if (rating <= 8){
            System.out.println("I recommend 'Toy Story'.");
        }
        if (rating <= 7){
            System.out.println("I recommend 'Godzilla'.");
        }
    }
}
